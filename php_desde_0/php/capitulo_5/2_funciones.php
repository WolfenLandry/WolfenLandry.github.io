<h1>Funciones de usuario</h1>


<pre>
Una función no es mas que un fragmento de un programa al que, por razones de reusabilidad o de estructura, le asignamos un nombre 

Las funciones de un lenguaje nos permiten simplificar grandemente los problemas complejos , y gracias a ello, podemos escribir soluciones
a problemas complicados.

¿para que  usar funciones?
Uno de los objetivos fundamentales de una función es la reutilización, es decir, la posiblidad de no tener que escribir una secuencia de intrucciones similares una 
instrucciones una y otra vez.

Una segunda razon  es la llamada "estructuración". Es decir que mediante la implementacion de funciones, podemos dividir la resolucion de un 
problema complejo en partes mas simples, de modeo que podemos  programar, probar y eventualmente integrar esar partes y lograr nuestros objetivos

Cuando un programa esta desarrollados en mode estructurado, es decir, debidamente clasificado y dividido en segmentos basicos, 
el resultado
es mucho mas facil de mantener, ya que dependiendo del tipo de errores que pudieramos tener, nos sera simepre mas facil encontrar el segmento 
causante de la falla y repararlo

<i></i>
<h2>sintaxis de una función </h2>
Toda función de usutio comienza con la palabra <i>function</i> y el nombre de la función.
encerrado entre parentesis, aparece la lista e parametros seprados por coma.
luego viene la secuencia de comandos que conforme la función en si encerrados entre llaves.

Una funcoón puede o no retornar un valor al ser ejecutada. de ser asi, debemos asegurarnos de colocar 
al menos un comando <i>return</i> antes del final de la función 
este comando indica el valor a ser retornado.


