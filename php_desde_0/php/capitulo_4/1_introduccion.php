Las <i>estructuras de control</i> en un lenguaje de programación son las que permiten 
alterar el <i>orden</i> de <i>ejecución</i> de los comandos 

Una <i>estructuras de control</i> puede permitir que <i>ejecutemos</i> un conjunto de 
instrucciones <i>solo si</i> se ha cumplido una cierta condición

Podemos adcionalmente <i>indicar una o mas</i> instrucción que deben <i>ejecutarse</i> si la <i>condición</i> 
<i>no se cumnple</i> tambien existen otros tipos de estructuras de control que permiten <i>repetir</i> un 
<i>cierto trozo</i> del codigo por un cierto numero de veces o <i>hasta</i> que se <i>cumpla</i> cierta <i>condición</i> 

En lineas generales podriamos decir que una <i>estructuras de control</i> define una <i>condicion</i> 
que de <i>cumplirse</i>, habrá de ejecutarse un <i>comando</i> o un <i>bloque definido</i> a continuacion de la misma 

Un <i>bloque</i> no es otra cosa que <i>dos o mas comandos</i> separados por el punto y coma y encerrados entre 
los simbolos de llave, si no existe un bloque luego de una estructura de control, entonces se esta 
condicionando solo la linea siguiente 	
